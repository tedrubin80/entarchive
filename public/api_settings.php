// Helper function to safely include config
function safeInclude($file) {
    $paths = [
        $file,
        __DIR__ . '/' . $file,
        __DIR__ . '/../' . $file,
        '../' . $file
    ];
    
    foreach ($paths as $path) {
        if (file_exists($path)) {
            require_once $path;
            return true;
        }
    }
    return false;
}

